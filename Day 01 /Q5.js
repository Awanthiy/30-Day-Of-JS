// Declare variables with different data types
let name = "Ava";
let age = 23;
let isRegistered = true;
let selectedColor = null;
let favoriteNumber; // This will be undefined

// Check the data type of each variable using typeof
console.log(typeof name);      // Output: string
console.log(typeof age);        // Output: number
console.log(typeof isRegistered); // Output: boolean
console.log(typeof selectedColor); // Output: object (**Important note**)
console.log(typeof favoriteNumber); // Output: undefined
