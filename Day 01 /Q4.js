// Declare a variable with a string value
let message = "This is a string.";

// Declare a variable with a boolean value (true)
let isRunning = true;

// Declare a variable without assigning a value (undefined)
let maybeLater;

// Declare a variable and explicitly assign null
let nothingHere = null;

console.log(message);  // Output: This is a string.
console.log(isRunning); // Output: true
console.log(maybeLater); // Output: undefined
console.log(nothingHere); // Output: null
